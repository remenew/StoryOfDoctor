def get_description_and_usage(herb_name):
    """
    获取药材描述和用途，如果知识库中有则返回，否则生成默认值
    """
    herb_name = herb_name.strip()
    
    # 完全匹配
    if herb_name in herb_knowledge:
        return herb_knowledge[herb_name]['desc'], herb_knowledge[herb_name]['use']
    
    # 部分匹配（比如去掉"仁"、"子"等后缀）
    for name in herb_knowledge:
        if herb_name.startswith(name) or name.startswith(herb_name):
            return herb_knowledge[name]['desc'], herb_knowledge[name]['use']
    
    # 如果不匹配，生成默认描述
    if herb_name.endswith('仁'):
        return f'{herb_name}的种仁', f'润肠通便'
    elif herb_name.endswith('子'):
        return f'{herb_name}的种子', f'消食化痰'
    elif herb_name.endswith('皮'):
        return f'{herb_name}的树皮', f'清热燥湿'
    elif herb_name.endswith('根'):
        return f'{herb_name}的干燥根', f'清热燥湿'
    elif herb_name.endswith('叶'):
        return f'{herb_name}的干燥叶', f'疏散风热'
    elif herb_name.endswith('花'):
        return f'{herb_name}的干燥花', f'清热解毒'
    elif herb_name.endswith('炭'):
        return f'{herb_name}煅炭而成', f'收敛止血'
    elif herb_name.endswith('炭'):
        return f'{herb_name}煅炭而成', f'收敛止血'
    elif herb_name.endswith('霜'):
        return f'{herb_name}加工制成的结晶', f'清热泻火'
    elif herb_name.endswith('油'):
        return f'{herb_name}的油脂', f'润燥滑肠'
    elif herb_name.endswith('草'):
        return f'{herb_name}的干燥全草', f'清热解毒'
    elif herb_name.endswith('木'):
        return f'{herb_name}的木材', f'理气'
    elif herb_name.endswith('仁'):
        return f'{herb_name}的种仁', f'固精缩尿'
    elif herb_name.endswith('鞘'):
        return f'{herb_name}的干燥卵鞘', f'补肾助阳'
    elif herb_name.endswith('壳'):
        return f'{herb_name}的外壳', f'理气止痛'
    else:
        return f'{herb_name}的干燥药用部位', f'清热解毒'

def main():
    # 读取 Excel 文件
    print("正在读取 herbs-0401.xlsx...")
    wb = load_workbook('d:/game/med/data/herbs-0401.xlsx')
    ws = wb.active
    
    # 找到 desc 和 use 列
    header_row = list(ws.iter_rows(min_row=1, max_row=1, values_only=True))[0]
    
    desc_col_idx = None
    use_col_idx = None
    name_col_idx = None
    
    for idx, cell_value in enumerate(header_row, 1):
        if cell_value == 'desc':
            desc_col_idx = idx
        elif cell_value == 'use':
            use_col_idx = idx
        elif cell_value == 'name':
            name_col_idx = idx
    
    if not all([desc_col_idx, use_col_idx, name_col_idx]):
        print("错误：未找到所需列")
        print(f"  name: {name_col_idx}, desc: {desc_col_idx}, use: {use_col_idx}")
        return
    
    print(f"找到列: name={name_col_idx}, desc={desc_col_idx}, use={use_col_idx}")
    
    # 找出 desc 为空的行
    empty_desc_rows = []
    max_row = ws.max_row
    
    for row in range(2, max_row + 1):
        desc_cell = ws.cell(row=row, column=desc_col_idx)
        if desc_cell.value is None or str(desc_cell.value).strip() == '':
            name_cell = ws.cell(row=row, column=name_col_idx)
            herb_name = str(name_cell.value).strip() if name_cell.value else ''
            if herb_name:
                empty_desc_rows.append((row, herb_name))
    
    print(f"\n找到 {len(empty_desc_rows)} 行 desc 列为空")
    
    # 填充描述和用途
    filled_count = 0
    for row, herb_name in empty_desc_rows:
        desc, use = get_description_and_usage(herb_name)
        ws.cell(row=row, column=desc_col_idx, value=desc)
        ws.cell(row=row, column=use_col_idx, value=use)
        print(f"  {row}: {herb_name} -> desc='{desc}', use='{use}'")
        filled_count += 1
    
    print(f"\n已填充 {filled_count} 行")
    
    # 保存文件
    wb.save('d:/game/med/data/herbs-0401.xlsx')
    print("\n保存完成！")
    wb.close()

if __name__ == '__main__':
    main()
